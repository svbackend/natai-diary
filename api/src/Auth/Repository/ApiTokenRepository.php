    public function findUserByToken(string $apiToken): ?User
    {
        /** @var ApiToken|null $token */
        $token = $this->createQueryBuilder('t')
            ->innerJoin('t.user', 'u')
            ->andWhere('t.apiToken = :apiToken')
            ->setParameter('apiToken', $apiToken)
            ->getQuery()
            ->getOneOrNullResult()
        ;

        if ($token?->isValid()) {
            return $token->getUser();
        }

        return null;
    }
